							<div class="single-post-content">

                <p>Следующим шагом для студентов после окончания учёбы является трудоустройство на хорошую работу. Чтобы получить заветное рабочее место, необходимо подготовить резюме (или как еще говорят - «CV, Circulum Vitae»), которое представит вас в выгодном свете перед работодателем. </p>
<div class="free-space"></div>
                <h3>1. Указать ключевые моменты </h3>

              <p>  Это основные пункты, которые обязательно должны быть указаны в любом CV:</p>

              <ul>
              <li>  Полное имя</li>
                <li>Рабочее место в настоящий момент или статус «Выпускник/Студент»</li>
                <li>Аккаунт LinkedIn (поверьте, это заинтересует вашего работодателя) </li>
              <li>  Адрес</li>
              <li>  Контактный телефон</li>
              <li>  Email</li>
              <li>  Образование</li>
                <li>Опыт работы</li>
              <li>  Навыки (касающиеся именно вашей сферы деятельности)</li>
              </ul>
                <div class="free-space"></div>
                <h3>2. Ваши бонусные очки </h3>
                <ul>
                <li>  Знание иностранных языков</li>
                <li>  Интересы/Опыт, также относящиеся к сфере деятельности</li>
                </ul>
                <div class="free-space"></div>
              <h3>  3. Ответ на вопрос </h3>

                <p>Часто в объявлении работодатель пишет, какие навыки требуются от сотрудника. Если вы соответствуете запросам, обязательно укажите это в резюме. </p>
<div class="free-space"></div>
                <h3>4. У каждого должно быть 3 резюме</h3>

                <p>Резюме 1 - всё, что вы когда-либо писали о себе. Весь опыт, все рабочие места и все достижения. Это позволит вам просто копировать нужные секции и использовать для написание в резюме, специализированным под рабочее место. </p>

                <p>Резюме 2 - своеобразный шаблон, содержащий только самую основную информацию о вас. Имя, квалификация, образование, информация, которая не меняется в обозримом будущем. Этот шаблон будет использоваться, как шаблон в последствии. </p>

                <p>Резюме 3 - то самое, которые вы отправите работодателю. Берете резюме 1, дополняете его подходящей к месту работы информацией из резюме 2 и в итоге получается прекрасное резюме, отвечающее всем требованиям. </p>
<div class="free-space"></div>
                <h3>5. Краткость - сестра таланта</h3>

                <p>Работодатель прочитывает сотни CV и это на самом деле выматывает. Не стоит хвалиться тем, что вы забили хет-трик в финале турнира «Кожаный мяч», если это не относится к сфере вашей деятельности. </p>

                <p>Постарайтесь уложиться в одну страницу А4. (Ну если у вас 25-летний опыт в профессии, можно на 3 страницы.)</p>
<div class="free-space"></div>
              <h3>  6. Будьте креативны</h3>

                <p>Этот совет относится только к специалистам, чей род деятельности связан так или иначе с творчеством. Если вы дизайнер, креативность выделит вас из серой массы. </p>

                <p>Но если вы подаете резюме на должность в индустрии финансов или бизнеса, тут предпочтительнее строгий формат. </p>
<div class="free-space"></div>
                <h3>7. Строгий формат не значит скучный</h3>

                <p>Строгость и сдержанность не обязывает вас писать все исключительно шрифтом Times New Roman с размером 10. </p>

                <p>Подберите интересный шрифт для своего имени. Пусть он зацепит внимание. А потом возвращайтесь к более сдержанному оформлению. </p>
<div class="free-space"></div>
              <h3>  8. Добавлять фото или нет? </h3>

                <p>Сейчас ведутся большие дискуссии на эту тему и к окончательному выводу так никто и не пришел. Тем не менее, добавить фото в CV - это дополнительный риск, так как на рынке еще достаточно консервативных соискателей, которые не приемлют такой формат. </p>

                <p>В общем, используйте фото только если это крайне необходимо и ваша интуиция подсказывает вам, что это единственно верное решение. </p>
<div class="free-space"></div>
              <h3>  9. Перечитайте 5 раз</h3>

                <p>Попросите кого-нибудь проверить ваше резюме на все виды ошибок. Грамотность, грамотность и еще раз грамотность - обязательное условие. </p>
                <p>Проверьте, правильно ли вы указали свои контакты. </p>


