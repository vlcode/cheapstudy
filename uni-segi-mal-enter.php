                <h3>Поступление</h3>
<p>Процесс зачисления и утверждение студенческой визы занимает около 10,5 недель.</p>

<p><em>SEGI настоятельно рекомендует начинать процесс поступления минимум за 3 месяца до начала семестра во избежание опоздания на начало занятий.</em></p>
<p>Помните, что все студенты, прибывающие в Малайзию на учебу, должны предъявить письмо-подтверждение студенческой визы (Immigration Approval Letter) при прохождении иммиграционного контроля.</p>
<p><strong>Подача заявления</strong></p>
<p>Для поступления в университет и получения студенческой визы необходимо направить на электронный адрес office@rus-austria.com сканированные в формате PDF и JPG следующие документы:</p>
<ol>
<li>Оригинал и нотариально заверенный период академической квалификации (аттестат об окончании средней школы с (приложением/транскриптом), диплом среднего специального или высшего образования или другой документ, если имеется).</li>
<li>Сертификат теста IELTS и TOEFL по английскому языку (при наличии)</li>
<li>Все страницы паспорта (включая пустые)</li>
<li>Фотографию паспортного размера 3,5Х5 см. На голубом фоне в формате JPG с высоким разрешением</li>
<li>Резюме и два (2) рекомендательных письма на английском языке или их перевод (только для поступления на программу МВА). Нотариально заверенный перевод рекомендательных писем не требуется.</li>
</ol>
<p><strong>Рассмотрение документов и получение студенческой визы</strong></p>
<ol start="2100">
<li>После предварительного рассмотрения документов (два рабочих дня) и положительного решения университета о зачислении, абитуриенту необходимо произвести оплату за рассмотрение документов и визы, именнуемый вступительный взнос в сумме RM2100.00.</li>
</ol>
<p>Сканированную копию банковского платёжного документа об оплате необходимо переслать на info@kmeg.kz. В письме необходимо указать выбор комнаты в общежитие для последующего резервирования.</p>
<p><strong>III.</strong> После получения оплаты вступительного взноса, в течение 2-х-недель Университет подготовит письмо-уведомление (Letter of Offer) о зачислении абитуриента в Университет, с указанием необходимой суммы оплаты за обучение по выбранной программе. Письмо будет направлено на электронный адрес абитуриента.</p>
<ol>
<li>В период подготовки Университета письма-уведомления о зачисление, необходимо направить фото паспортного размера курьерской почтой на адрес Университета.</li>
<li>По мере получения фотографии абитуриента, Университет падает документы на оформление студенческой визы (процесс занимает от 4-8 недель). После утверждение визы, абитуриенту необходимо оплатить первую часть стоимости обучения, указанной в письме о зачисление в Университет и осуществить оплату за проживание. После поступление оплаты на счет Университета, письмо-подтверждение визы (Immigration Approval Letter) иммиграционной службой Малайзии, а также оригинал письма о зачисление абитуриента в Университет (Letter of Offer) будут высланы курьерской почтой и по электронному адресу абитуриента.</li>
<li>Копия билета необходимо ОБЯЗАТЕЛЬНО направить на электронный адрес info@kmeg.kz, не позднее чем за десять (10) дней до прибытия в Малайзию для организации встречи студента в международном аэропорту Куала Лумпур (KLIA).</li>
</ol>
<p><strong>Прибытие в Малайзию</strong></p>
<p>По прилёту в Малайзию, необходимо сесть на поезд (Aerotrain), соединяющий два терминала аэропорта, который доставит в главный терминал аэропорта, где необходимо пройти иммиграционные службы (паспортный контроль) и таможенные процедуры.</p>
<p>Для прохождения иммиграционного контроля студенту необходимо предоставить следующие документы:</p>
<ol>
<li>Паспорт (срок действия паспорта должен быть не менее года)</li>
<li>Письмо-уведомление (Letter of Offer) о зачислении студента в университет/колледж</li>
<li>Письмо-подтверждение студенческой визы (Immigration Approval Letter)</li>
<li>Оригиналы и нотариально заверенные переводы академических квалификаций, копии которые были направлены в Университет ранее.</li>
</ol>


