              <p>    Томми Осет из Норвегии только приступил к своей учебе в Dongbei University of Finance and Economics  в городе Далянь.</p>

  <p>  Вот как он описывает свои первые 2 месяца в Китае… </p>

   <img src="img/blog/pereezd-v-kitay-bez-kupyur1.jpg" alt="pereezd-v-kitay-bez-kupyur" class="img-responsive">
    <div class="free-space"></div>
  <p>  В такси, которое везло меня из аэропорта, я впервые полностью осознал «Я на самом деле в Китае!». </p>

  <p>  Очень много вывесок с китайскими иероглифами повсюду и ощущение, что город не спит даже ночью. Постоянное непрерывное движение. </p>

    <img src="img/blog/pereezd-v-kitay-bez-kupyur2.png" alt="pereezd-v-kitay-bez-kupyur" class="img-responsive">
    <div class="free-space"></div>
  <p>  Далянь - очень развитый город со всей необходимой инфраструктурой. Здесь есть как китайские, так и западные магазины. Starbucks, Mcdonald’s, Burger King, KFC, метро - всё это в порядке вещей. </p>

  <p>  В плане еды мне лично всё нравится и к тому же пища цены очень доступные. Такое разнообразие блюд мой желудок увидел впервые. </p>

  <p>  На третий день я встретил 2х студентов, которые теперь являются моими соседями по комнате и одногруппниками. Первым делом мы решили прогуляться по городу и это был прекрасный день, полный гаммой впечатлений. И все это несмотря на то, что ни один из нас не знал ни слова на китайском.  </p>

    <img src="img/blog/pereezd-v-kitay-bez-kupyur3.jpg" alt="pereezd-v-kitay-bez-kupyur" class="img-responsive">
    <div class="free-space"></div>
  <p>  Мы сели в автобус и попали в самый час-пик. Тесно было настолько, что казалось, будто люди залезают друг на друга в общественном транспорте.</p>

  <p>  Доехав до центра города мы обнаружили, что нас ждет ещё один сюрприз. Мы обнаружили, что люди вокруг пялятся на нас, как на инопланетян. Кто-то даже подходил сфотографироваться с нами. </p>

  <p>  На самом деле, я приехал в Китай с целью изучить китайский язык и его культуру. Но на деле, прожив тут несколько месяцев, я познакомился с стольким количеством представителей различных культур и услышал столько иностранных языков, чего точно не мог ожидать даже в самых своих амбициозных планах. </p>

    <img src="img/blog/pereezd-v-kitay-bez-kupyur4.jpg" alt="pereezd-v-kitay-bez-kupyur" class="img-responsive">
    <div class="free-space"></div>
    <p>Чтобы помочь вам представить, как я познакомился со здешним набором культур, я расскажу вам история с прошлого воскресенья: Встаю я в 8 утра, на диване неподалеку спит мой сосед по комнате из Панамы, на другом диване храпит мой английский друг, ну а на третьем расположилась подруга из Франции. </p>

  <p>  Примерно в 10 часов того же дня я ушел на 8-ми часовой тур с 4 девушками из Тайланда. Когда мы вернулись домой, мой другой сосед устроил в нашей квартире традиционный вечер Зимбабве. </p>

  <p>  Если подытожить, могу точно сказать: Студенческая жизнь в Китае - это самый грандиозный опыт в моей жизни. И я уверен, те 4 года, которые я буду тут учиться, будут наполнены запоминающимися приключениями и прекрасными воспоминаниями.</p>


